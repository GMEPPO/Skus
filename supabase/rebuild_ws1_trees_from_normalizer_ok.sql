create temp table _resolved_words (
  row_id integer not null,
  reference_code text not null,
  family_code text not null,
  category text not null,
  code text not null,
  word_id uuid not null
);

-- Resuelve cada segmento contra skus_words. Si un codigo tiene varias palabras,
-- prioriza la palabra cuyo label/designation aparece en la designacion normalizada.
insert into _resolved_words (row_id, reference_code, family_code, category, code, word_id)
select distinct
  source.row_id,
  source.reference_code,
  source.family_code,
  source.category,
  source.code,
  picked.word_id
from (
  select row_id, reference_code, designation_pt, family_code, 'format' as category, format_code as code from _ok_segments where format_code <> '000'
  union all
  select row_id, reference_code, designation_pt, family_code, 'product' as category, product_code as code from _ok_segments where product_code <> '000'
  union all
  select row_id, reference_code, designation_pt, family_code, 'size' as category, size_code as code from _ok_segments where size_code <> '000'
  union all
  select row_id, reference_code, designation_pt, family_code, 'packaging' as category, packaging_code as code from _ok_segments where packaging_code <> '000'
  union all
  select row_id, reference_code, designation_pt, family_code, 'extra' as category, extra_code as code from _ok_segments where extra_code <> '000'
) source
join public.skus_field_types ft on ft.code = source.category
cross join lateral (
  select w.id as word_id
  from public.skus_words w
  where w.default_field_type_id = ft.id
    and w.reference_code = source.code
    and w.is_active = true
  order by
    case
      when lower(source.designation_pt) like '%' || lower(w.label) || '%' then 0
      when lower(source.designation_pt) like '%' || lower(coalesce(w.designation_pt, w.designation, w.label)) || '%' then 1
      else 2
    end,
    length(coalesce(w.designation_pt, w.designation, w.label)) desc,
    w.label
  limit 1
) picked;

-- Asociaciones palabra-familia.
insert into public.skus_word_families (word_id, family_id)
select distinct
  rw.word_id,
  f.id
from _resolved_words rw
join public.skus_families f on f.reference_code = rw.family_code
on conflict do nothing;

-- Palabras por nivel del arbol.
insert into public.skus_family_tree_level_words (tree_level_id, word_id, sort_order)
select distinct
  lvl.id,
  rw.word_id,
  case rw.category
    when 'format' then 1000
    when 'product' then 2000
    when 'size' then 3000
    when 'packaging' then 4000
    when 'extra' then 5000
  end + (dense_rank() over (partition by rw.family_code, rw.category order by rw.code, rw.word_id))::integer
from _resolved_words rw
join public.skus_families f on f.reference_code = rw.family_code
join public.skus_family_tree_versions tv on tv.id = f.active_tree_version_id
join public.skus_family_tree_levels lvl on lvl.tree_version_id = tv.id
join public.skus_field_types ft on ft.id = lvl.field_type_id and ft.code = rw.category
on conflict (tree_level_id, word_id) do update
set sort_order = excluded.sort_order;

create temp table _row_words as
select
  s.row_id,
  s.reference_code,
  s.family_code,
  f.active_tree_version_id as tree_version_id,
  (max(rw.word_id::text) filter (where rw.category = 'format'))::uuid as format_word_id,
  (max(rw.word_id::text) filter (where rw.category = 'product'))::uuid as product_word_id,
  (max(rw.word_id::text) filter (where rw.category = 'size'))::uuid as size_word_id,
  (max(rw.word_id::text) filter (where rw.category = 'packaging'))::uuid as packaging_word_id,
  (max(rw.word_id::text) filter (where rw.category = 'extra'))::uuid as extra_word_id
from _ok_segments s
join public.skus_families f on f.reference_code = s.family_code
left join _resolved_words rw on rw.row_id = s.row_id
group by s.row_id, s.reference_code, s.family_code, f.active_tree_version_id;

create temp table _level_ids as
select
  f.reference_code as family_code,
  tv.id as tree_version_id,
  (max(lvl.id::text) filter (where ft.code = 'format'))::uuid as format_level_id,
  (max(lvl.id::text) filter (where ft.code = 'product'))::uuid as product_level_id,
  (max(lvl.id::text) filter (where ft.code = 'size'))::uuid as size_level_id,
  (max(lvl.id::text) filter (where ft.code = 'packaging'))::uuid as packaging_level_id,
  (max(lvl.id::text) filter (where ft.code = 'extra'))::uuid as extra_level_id
from public.skus_families f
join public.skus_family_tree_versions tv on tv.id = f.active_tree_version_id
join public.skus_family_tree_levels lvl on lvl.tree_version_id = tv.id
join public.skus_field_types ft on ft.id = lvl.field_type_id
where f.reference_code in (select distinct family_code from _ok_segments)
group by f.reference_code, tv.id;

-- Edges observados en referencias OK. Los niveles con 000 quedan como opcion vacia del generador.
insert into public.skus_family_tree_edges (
  tree_version_id,
  from_level_id,
  from_word_id,
  to_level_id,
  to_word_id,
  is_active
)
select distinct rw.tree_version_id, l.format_level_id, rw.format_word_id, l.product_level_id, rw.product_word_id, true
from _row_words rw join _level_ids l on l.family_code = rw.family_code
where rw.format_word_id is not null and rw.product_word_id is not null
union
select distinct rw.tree_version_id, l.product_level_id, rw.product_word_id, l.size_level_id, rw.size_word_id, true
from _row_words rw join _level_ids l on l.family_code = rw.family_code
where rw.product_word_id is not null and rw.size_word_id is not null
union
select distinct rw.tree_version_id, l.size_level_id, rw.size_word_id, l.packaging_level_id, rw.packaging_word_id, true
from _row_words rw join _level_ids l on l.family_code = rw.family_code
where rw.size_word_id is not null and rw.packaging_word_id is not null
union
select distinct rw.tree_version_id, l.packaging_level_id, rw.packaging_word_id, l.extra_level_id, rw.extra_word_id, true
from _row_words rw join _level_ids l on l.family_code = rw.family_code
where rw.packaging_word_id is not null and rw.extra_word_id is not null
on conflict (tree_version_id, from_level_id, from_word_id, to_level_id, to_word_id) do update
set is_active = true;

commit;

