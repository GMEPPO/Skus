create temp table _ok_segments as
select distinct
  row_id,
  reference_code,
  designation_pt,
  substring(reference_code from 1 for 3) as family_code,
  substring(reference_code from 4 for 3) as format_code,
  substring(reference_code from 7 for 3) as product_code,
  substring(reference_code from 10 for 3) as size_code,
  substring(reference_code from 13 for 3) as packaging_code,
  substring(reference_code from 16 for 3) as extra_code
from _ok_sku_refs
where reference_code ~ '^[A-Z0-9&.]{18}$'
  and substring(reference_code from 1 for 3) <> '000';

create temp table _target_families as
select f.id, f.reference_code
from public.skus_families f
where f.status = 'active'
  and f.reference_code is not null
  and (
    f.reference_code in (select distinct family_code from _ok_segments)
    or f.description = 'Familia SKU importada de WS1 1.xlsx'
  );

-- Asegura versiones y niveles fijos para las familias OK.
insert into public.skus_family_tree_versions (family_id, version_number, status, published_at)
select
  f.id,
  coalesce((select max(tv.version_number) from public.skus_family_tree_versions tv where tv.family_id = f.id), 0) + 1,
  'published',
  now()
from public.skus_families f
where f.reference_code in (select distinct family_code from _ok_segments)
  and f.status = 'active'
  and not exists (
    select 1
    from public.skus_family_tree_versions tv
    where tv.family_id = f.id
      and tv.status = 'published'
  );

update public.skus_families f
set active_tree_version_id = (
      select tv.id
      from public.skus_family_tree_versions tv
      where tv.family_id = f.id
        and tv.status = 'published'
      order by tv.version_number desc
      limit 1
    ),
    updated_at = now()
where f.reference_code in (select distinct family_code from _ok_segments)
  and f.status = 'active';

insert into public.skus_family_tree_levels (
  tree_version_id,
  field_type_id,
  level_order,
  label_override,
  is_required,
  designation_included
)
select
  f.active_tree_version_id,
  ft.id,
  case ft.code
    when 'format' then 1
    when 'product' then 2
    when 'size' then 3
    when 'packaging' then 4
    when 'extra' then 5
  end,
  null,
  true,
  true
from public.skus_families f
cross join public.skus_field_types ft
where f.reference_code in (select distinct family_code from _ok_segments)
  and f.status = 'active'
  and f.active_tree_version_id is not null
  and ft.code in ('format', 'product', 'size', 'packaging', 'extra')
on conflict (tree_version_id, level_order) do update
set field_type_id = excluded.field_type_id,
    label_override = excluded.label_override,
    is_required = true,
    designation_included = true;

-- Limpia asociaciones antiguas de familias WS1 importadas y familias con OK.
